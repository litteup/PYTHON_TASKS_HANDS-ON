
lengthInCentimeter = float(input("Enter length in centimeter: "))
lengthInMeters = format((lengthInCentimeter * 0.001),".2f")
lengthInKilometers =  format((lengthInCentimeter * 0.00001),".2f")


print(f'Length in Meter = {lengthInMeters} m')
print(f'Length in Kilometer = {lengthInKilometers} km')




